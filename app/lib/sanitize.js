// Sanitize function to prevent XSS attacks
// Replaces dangerous HTML characters with safe equivalents
// so browser will display them as text instead of executing
export function sanitize(value) {
  if (typeof value !== "string") return "";
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#x27;")
    .replace(/\//g, "&#x2F;");
}
